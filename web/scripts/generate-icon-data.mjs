function resolveIconData(collection, sourceName) {
  const icon = collection.icons[sourceName]
  if (!icon) {
    throw new Error(`Missing icon ${collection.prefix}:${sourceName}`)
  }

  const { hidden, ...rest } = icon

  return {
    ...rest,
    width: rest.width ?? collection.width,
    height: rest.height ?? collection.height,
    left: rest.left ?? collection.left,
    top: rest.top ?? collection.top,
  }
}

const bundledIcons = {}
for (const [collection, entries] of ICON_SOURCES) {
  for (const [targetName, sourceName] of entries) {
    bundledIcons[targetName] = resolveIconData(collection, sourceName)
  }
}

const content = `import type { IconifyIcon } from '@iconify/types'\n\n// Generated by \`bun ./scripts/generate-icon-data.mjs\`.\nexport const BUNDLED_ICONS: Record<string, IconifyIcon> = ${JSON.stringify(bundledIcons, null, 2)}\n`
writeFileSync(new URL('../src/lib/icon-data.ts', import.meta.url), content)
